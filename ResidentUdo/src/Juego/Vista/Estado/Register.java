    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        // TODO add your handling code here:
        int resultado = Cargador.register(jTextFieldUser.getText(), jTextFieldPass.getText());
        switch (resultado) {
            case Cargador.USER_TOO_LONG:
                Opcion.showMessage(ventana, true, "M√°ximo " + Cargador.MAX_CARACTERES + " caracteres");
                break;
            case Cargador.USER_INVALID:
                Opcion.showMessage(ventana, true, "Usuario inv√°lido");
                break;
            case Cargador.PASS_INVALID:
                Opcion.showMessage(ventana, true, "Contrase√±a no permitida");
                break;
            case Cargador.USER_EXIST:
                Opcion.showMessage(ventana, true, "Este usuario ya existe");
                break;
            case Cargador.USER_CREATED:
                Opcion.showMessage(ventana, true, "Usuario creado con √©xito");
                ventana.cargarNuevoUsuario(jTextFieldUser.getText());
                ventana.guardarSesion(jTextFieldUser.getText(), jTextFieldPass.getText(), jCheckBoxMantenerSesion.isSelected(), jCheckBoxRecordarSesion.isSelected());
                ventana.setContentPane("Datos");
                break;
            default:
                Opcion.showMessage(ventana, true, "Error");
                break;
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

